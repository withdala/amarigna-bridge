// Function to transliterate text
export function transliterate(text: string): string {
  let result = '';
  let i = 0;
  
  while (i < text.length) {
    let found = false;
    // Try matching 3 characters, then 2, then 1
    for (let len = 3; len >= 1; len--) {
      const chunk = text.substring(i, i + len);
      if (amharicMap[chunk]) {
        result += amharicMap[chunk];
        i += len;
        found = true;
        break;
      }
    }
    
    if (!found) {
      result += text[i];
      i++;
    }
  }
  
  return result;
}